    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(tablaUsuarios.getRowCount() > 0){
            if(tablaUsuarios.getSelectedRow() != -1){
                int id_usuario = Integer.parseInt(String.valueOf(tablaUsuarios.getValueAt(tablaUsuarios.getSelectedRow(), 0)));
               
                Modificar pantallaEdit = new Modificar(control,id_usuario);
                pantallaEdit.setVisible(true);
                pantallaEdit.setLocationRelativeTo(null);
            }
            else
                mostrarMensaje("No selecciono ningun registro", "Error", "Error al modificar");
        }
        else
            mostrarMensaje("La tabla no tiene registros", "Error", "Error al modificar");
    }//GEN-LAST:event_btnEditarActionPerformed

